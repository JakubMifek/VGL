    fun toBlock(): Block {
        return Block(this, HashMap())
    }

    companion object {
        private val map = HashMap<Int, Blocks>()

        @Throws(Error::class)
        fun getById(id: Int): Blocks {
            if (id !in map.keys) {
                map[id] = values().find { it.id == id } ?: throw Error("Block with ID $id was not found.")
            }

            return map[id]!!
        }
    }
